using System.Text;
using Paramore.Brighter.SourceGenerators.Model;

namespace Paramore.Brighter.SourceGenerators;

/// <summary>
/// Pure function that turns a <see cref="RegistrationModel"/> into the C# source for the
/// partial method implementation. Holds no Roslyn references, so it can be unit-tested
/// without constructing a Compilation.
/// </summary>
public static class RegistrationWriter
{
    public static string Write(RegistrationModel model)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        var hasNamespace = !string.IsNullOrEmpty(model.Namespace);
        if (hasNamespace)
        {
            sb.Append("namespace ").Append(model.Namespace).AppendLine();
            sb.AppendLine("{");
        }

        WriteContainingTypeOpen(sb, model);
        WriteMethodSignature(sb, model);

        WriteHandlers(sb, model.ParameterName, model.Handlers, isAsync: false);
        WriteHandlers(sb, model.ParameterName, model.AsyncHandlers, isAsync: true);
        WriteMappers(sb, model.ParameterName, model.Mappers, model.AsyncMappers);
        WriteTransforms(sb, model.ParameterName, model.Transforms);

        sb.Append("            return ").Append(model.ParameterName).AppendLine(";");
        sb.AppendLine("        }");
        sb.AppendLine("    }");

        if (hasNamespace)
            sb.AppendLine("}");

        return sb.ToString();
    }

    private static void WriteContainingTypeOpen(StringBuilder sb, RegistrationModel model)
    {
        var typeKeyword = (model.ContainingTypeIsStatic, model.IsPartial) switch
        {
            (true, true) => "static partial class",
            (true, false) => "static class",
            (false, true) => "partial class",
            (false, false) => "class",
        };
        sb.Append("    ")
            .Append(model.ContainingTypeAccessibility)
            .Append(' ')
            .Append(typeKeyword)
            .Append(' ')
            .AppendLine(model.ContainingTypeName);
        sb.AppendLine("    {");
    }

    private static void WriteMethodSignature(StringBuilder sb, RegistrationModel model)
    {
        sb.Append("        ").Append(model.MethodAccessibility).Append(" static ");
        if (model.IsPartial)
            sb.Append("partial ");
        sb.Append(model.ReturnTypeFullyQualified).Append(' ').Append(model.MethodName).Append('(');
        if (model.IsExtensionMethod)
            sb.Append("this ");
        sb.Append(model.ParameterTypeFullyQualified).Append(' ').Append(model.ParameterName);
        sb.AppendLine(")");
        sb.AppendLine("        {");
    }

    private static void WriteHandlers(
        StringBuilder sb,
        string paramName,
        EquatableArray<HandlerEntry> entries,
        bool isAsync)
    {
        if (entries.Count == 0)
            return;

        var callbackMethod = isAsync ? "AsyncHandlers" : "Handlers";
        sb.Append("            ").Append(paramName).Append('.').Append(callbackMethod).AppendLine("(r =>");
        sb.AppendLine("            {");
        sb.AppendLine("                var registry = (global::Paramore.Brighter.Extensions.DependencyInjection.ServiceCollectionSubscriberRegistry)r;");

        foreach (var entry in entries)
        {
            if (entry.IsOpenGeneric)
            {
                sb.Append("                registry.EnsureHandlerIsRegistered(typeof(")
                    .Append(entry.HandlerTypeFullyQualified)
                    .AppendLine("));");
            }
            else
            {
                sb.Append("                registry.Add(typeof(")
                    .Append(entry.RequestTypeFullyQualified)
                    .Append("), typeof(")
                    .Append(entry.HandlerTypeFullyQualified)
                    .AppendLine("));");
            }
        }

        sb.AppendLine("            });");
    }

    private static void WriteMappers(
        StringBuilder sb,
        string paramName,
        EquatableArray<MapperEntry> sync,
        EquatableArray<MapperEntry> async)
    {
        if (sync.Count == 0 && async.Count == 0)
            return;

        sb.Append("            ").Append(paramName).AppendLine(".MapperRegistry(r =>");
        sb.AppendLine("            {");

        foreach (var entry in sync)
        {
            sb.Append("                r.Add(typeof(")
                .Append(entry.RequestTypeFullyQualified)
                .Append("), typeof(")
                .Append(entry.MapperTypeFullyQualified)
                .AppendLine("));");
        }

        foreach (var entry in async)
        {
            sb.Append("                r.AddAsync(typeof(")
                .Append(entry.RequestTypeFullyQualified)
                .Append("), typeof(")
                .Append(entry.MapperTypeFullyQualified)
                .AppendLine("));");
        }

        sb.AppendLine("            });");
    }

    private static void WriteTransforms(
        StringBuilder sb,
        string paramName,
        EquatableArray<string> transforms)
    {
        if (transforms.Count == 0)
            return;

        sb.Append("            ").Append(paramName).AppendLine(".Transforms(r =>");
        sb.AppendLine("            {");

        foreach (var transform in transforms)
        {
            sb.Append("                r.Add(typeof(")
                .Append(transform)
                .AppendLine("));");
        }

        sb.AppendLine("            });");
    }
}
