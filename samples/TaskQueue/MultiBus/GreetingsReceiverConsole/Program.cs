using System;
using Confluent.Kafka;
using Greetings.Ports.Commands;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.Hosting;
using Paramore.Brighter;
using Paramore.Brighter.MessagingGateway.Kafka;
using Paramore.Brighter.MessagingGateway.RMQ.Async;
using Paramore.Brighter.ServiceActivator.Extensions.DependencyInjection;
using Paramore.Brighter.ServiceActivator.Extensions.Hosting;

var builder = Host.CreateApplicationBuilder(args);

var subscriptions = new Subscription[]
{
    new KafkaSubscription<GreetingEvent>(
        new SubscriptionName("paramore.example.greeting"),
        channelName: new ChannelName("greeting.event"),
        routingKey: new RoutingKey("greeting.event"),
        groupId: "kafka-GreetingsReceiverConsole-Sample",
        numOfPartitions: 3,
        timeOut: TimeSpan.FromMilliseconds(100),
        offsetDefault: AutoOffsetReset.Earliest,
        commitBatchSize: 5,
        sweepUncommittedOffsetsInterval: TimeSpan.FromMilliseconds(10000),
        messagePumpType: MessagePumpType.Proactor),
    new RmqSubscription<AnotherGreetingEvent>(
        new SubscriptionName("paramore.example.another.greeting"),
        new ChannelName("another.greeting.event"),
        new RoutingKey("another.greeting.event"),
        timeOut: TimeSpan.FromMilliseconds(2000),
        isDurable: true,
        highAvailability: true,
        makeChannels: OnMissingChannel.Create,
        messagePumpType: MessagePumpType.Proactor), //change to OnMissingChannel.Validate if you have infrastructure declared elsewhere
};

//create the gateway
var consumerFactory = new KafkaMessageConsumerFactory(
    new KafkaMessagingGatewayConfiguration
    {
        Name = "paramore.brighter", BootStrapServers = new[] { "localhost:9092" }
    }
);

var rmqConnection = new RmqMessagingGatewayConnection
{
    AmpqUri = new AmqpUriSpecification(new Uri("amqp://guest:guest@localhost:5672")),
    Exchange = new Exchange("paramore.brighter.exchange")
};

var rmqMessageConsumerFactory = new RmqMessageConsumerFactory(rmqConnection);

builder.Services.AddConsumers(options =>
{
    options.Subscriptions = subscriptions;
    options.DefaultChannelFactory = new CombinedChannelFactory([
        new Paramore.Brighter.MessagingGateway.Kafka.ChannelFactory(consumerFactory),
        new Paramore.Brighter.MessagingGateway.RMQ.Async.ChannelFactory(rmqMessageConsumerFactory)
    ]);
}).AutoFromAssemblies();

builder.Services.AddHostedService<ServiceActivatorHostedService>();

var host = builder.Build();
await host.RunAsync();
